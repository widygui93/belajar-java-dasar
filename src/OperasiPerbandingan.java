public class OperasiPerbandingan {
    public static void main(String[] args) {
        int a = 100;
        int b = 100;

        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);

        String nama1 = "widy";
        String nama2 = "Widy";

        System.out.println(nama1 == nama2);
    }
}
